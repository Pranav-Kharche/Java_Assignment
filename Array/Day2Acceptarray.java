//5:Write a program to accept array of 5 numbers and display it.
public class Day2Acceptarray {

	public static void main(String[] args) {
		int[] arr=new int[5];
		arr[0]=11;
		arr[1]=22;
		arr[2]=33;
		arr[3]=44;
		arr[4]=55;
		System.out.println("Array elements are:");
		for(int i=0; i<5; i++)
		{
			System.out.println(arr[i]);
		}
	}
}
